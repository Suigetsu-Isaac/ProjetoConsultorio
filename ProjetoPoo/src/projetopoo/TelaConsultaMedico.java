    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
       try {
        // 1. Obter médico selecionado
       
        Medico medico = pegarMedico();
        // 2. Obter dia selecionado
        DayOfWeek dia = obterDiaSelecionado();
        LocalDate data = LocalDate.now().with(TemporalAdjusters.next(dia));

        // 3. Obter hora do spinner
        LocalTime hora = (LocalTime) jSpinner4.getValue();

        // 4. Verificar disponibilidade
        if (!verificarDisponibilidade(medico, dia, hora)) {
            throw new Exception("Horário indisponível!");
        }

        // 5. Verificar se o horário já está ocupado
        List<LocalTime> horariosOcupados = JSONManager.getHorariosOcupados(medico, data);
        if (horariosOcupados.contains(hora)) {
            throw new Exception("Horário já ocupado!");
        }

        String nomePaciente = (String) jComboBox1.getSelectedItem();    
        
        // 6. Criar e salvar consulta
        Consulta consulta = new Consulta(pegarPaciente(nomePaciente), medico, data, hora);
        JSONManager.salvarConsulta(consulta);

        JOptionPane.showMessageDialog(this, "Consulta agendada para:\n" +
                                            "Data: " + data + "\n" +
                                            "Horário: " + hora.format(DateTimeFormatter.ofPattern("HH:mm")));
    
       TelaMedico tela = new TelaMedico();
       tela.setVisible(true);
       dispose();
       } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    } 
    }//GEN-LAST:event_jButton1ActionPerformed

