    private void atualizarHorariosDisponiveis() {
    try {
        // 1. Obter médico selecionado
        
        
        Medico medico = pegarMedico();
        DayOfWeek dia = obterDiaSelecionado();
        
        if (medico != null && dia != null) {
            // 2. Obter a data correspondente ao dia selecionado
            LocalDate data = LocalDate.now().with(TemporalAdjusters.next(dia));

            // 3. Obter horários disponíveis do médico
            Set<LocalTime> horariosDisponiveis = medico.getHorariosDisponiveis(dia);

            // 4. Obter horários já ocupados
            List<LocalTime> horariosOcupados = JSONManager.getHorariosOcupados(medico, data);

            // 5. Filtrar horários disponíveis, removendo os ocupados
            Set<LocalTime> horariosLivres = horariosDisponiveis.stream()
                                                              .filter(hora -> !horariosOcupados.contains(hora))
                                                              .sorted()
                                                                .collect(Collectors.toCollection(LinkedHashSet::new));

            
            SpinnerModel model = new SpinnerListModel(new ArrayList<>(horariosLivres));
            jSpinner4.setModel(model);
        }
    } catch (Exception e) {
        // Se ocorrer algum erro, limpar o JSpinner
        jSpinner4.setModel(new SpinnerListModel());
    }
}
