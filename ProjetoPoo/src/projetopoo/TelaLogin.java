    private Usuario validarLoginPaciente(String user,String senha){
        
        for (Usuario p : pacientes ){
            if (user.equals(p.getNome()) && senha.equals(p.getSenha())){
               return p; 
            }
        }
        
        return null;
    }
    
    private Usuario validarLoginMedico(String user,String senha){
        
        for (Medico p : medicos ){
            if (user.equals(p.getNome()) && senha.equals(p.getSenha())){
               return p; 
            }
        }
        
        return null;
    }
    
